package ch27.collectionframework;

public class CollectionFramework {
    public static void main(String[] args) {
        /*
            컬렉션 프레임워크

            컬렉션 : 데이터 그룹의 의미
            프레임워크 : 표준화된 프로그래밍 방식을 의미
            컬렉션 프레임워크 : 데이터 그룹을 저장하는 클래스들을 표준화하여 놓은 것

            컬렉션 프레임워크의 모든 구성 요소들은 List, Map, Set 중 하나를 구현하고 있다.
            Vector, Stack, Hashtable, Properties 같은 클래스들은 오래 전에 존재하던 것이기에
            컬렉션 프레임워크의 이름을 지정하는 방식을 따르지 않았다.

            핵심 인터페이스
            Map, List, Set

            List
            순서가 있는 데이터의 집합이며 중복을 허용한다.
            ArrayList, LinkedList, stack, Vector 등이 존재한다.

            Set
            순서를 유지하지 않는 데이터 집합이다.
            데이터의 중복을 허용하지 않는다.
            HashSet, TreeSet 등이 있다.

            Map
            key, value의 쌍으로 이루어진 데이터의 집합이다.
            순서는 유지되지않으며, 키는 중복되지 않고, 밸류는 중복될 수 있다.
            HashMap, TreeMap, Hashtable, Properties 등이 있다.

        */
    }
}
